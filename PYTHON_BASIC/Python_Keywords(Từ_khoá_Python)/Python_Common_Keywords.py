#False	    Giá trị boolean cho biết "không đúng"

#None	    Biểu thị giá trị rỗng hoặc không có giá trị.

#True	    Giá trị boolean cho biết "có" hoặc "đúng".

#and	    Kết hợp hai điều kiện, cả hai đều phải đúng.

#as	        Được sử dụng khi đặt bí danh cho một mô-đun hoặc đối tượng.

#assert	    Được sử dụng để gỡ lỗi; kiểm tra xem một điều kiện có đúng hay không.

#break	    Thoát khỏi vòng lặp sớm

#class	    Dùng để định nghĩa một lớp (trong lập trình hướng đối tượng)

#def	   Định nghĩa một hàm

#del	    Xóa một biến, một mục trong danh sách hoặc một đối tượng.

#elif	    Else if – thêm một điều kiện khác sau ifcác câu lệnh

#else	    Phải làm gì nếu tất cả ifhoặc elifcác điều kiện đều sai

#except	    Xác định các bước cần thực hiện khi xảy ra lỗi cụ thể.

#finally	Một khối mã sẽ luôn được thực thi, bất kể có xảy ra ngoại lệ hay không.

#for	    Bắt đầu một vòng lặp chạy qua các mục.

#from	    Được sử dụng khi nhập các tác phẩm nghệ thuật cụ thể từ một mô-đun.

#if	        Chỉ chạy đoạn mã khi điều kiện được thỏa mãn.

#import 	Tải một mô-đun vào mã của bạn.

#in	        Kiểm tra xem một giá trị có tồn tại trong một chuỗi (ví dụ: danh sách, phạm vi, v.v.) hay không.

#is 	    Kiểm tra xem hai giá trị có tham chiếu đến cùng một đối tượng hay không.

#lambda	    Tạo một hàm nhỏ, ẩn danh

#nonlocal	Tham chiếu đến một biến trong phạm vi bên ngoài gần nhất.

#not	    Đảo ngược một điều kiện (Đúng trở thành Sai, và ngược lại)

#or	        Kết hợp các điều kiện; trả về true nếu ít nhất một điều kiện đúng.

#raise	    Buộc xảy ra lỗi

#return	    Trả về một giá trị từ một hàm.

#try	    Chạy mã và theo dõi lỗi.

#while	    Bắt đầu một vòng lặp chạy miễn là điều kiện vẫn đúng.

#with	    Được sử dụng để quản lý tài nguyên (ví dụ: tập tin) một cách an toàn.

#yield	    Trả về một giá trị từ một hàm tạo.